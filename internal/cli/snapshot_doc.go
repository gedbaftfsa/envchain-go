// Package cli snapshot commands.
//
// # snapshot
//
// Save a timestamped copy of a project's environment set:
//
//	envchain snapshot <project>
//
// # restore
//
// Restore a snapshot back to the original project name:
//
//	envchain restore <project@timestamp>
//
// # snapshots
//
// List all snapshots for a project:
//
//	envchain snapshots <project>
